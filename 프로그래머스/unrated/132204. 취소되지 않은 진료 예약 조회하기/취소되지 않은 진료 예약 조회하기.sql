-- 2022년 4월 13일 취소되지 않은 흉부외과(CS) 진료 예약 내역을 조회
SELECT APNT_NO, PT_NAME, PATIENT.PT_NO, T.MCDP_CD, DR_NAME, APNT_YMD FROM (
    SELECT APNT_NO, PT_NO, APPOINTMENT.MCDP_CD, DR_NAME, APNT_YMD FROM APPOINTMENT
    INNER JOIN DOCTOR ON DOCTOR.DR_ID = APPOINTMENT.MDDR_ID
    WHERE APPOINTMENT.MCDP_CD = "CS" AND DATE_FORMAT(APNT_YMD, "%Y-%m-%d") = "2022-04-13" AND APNT_CNCL_YN = "N") AS T
    INNER JOIN PATIENT ON PATIENT.PT_NO = T.PT_NO
    ORDER BY APNT_YMD