-- 진료예약일시를 기준으로 오름차순
SELECT APNT_NO, PT_NAME, T.PT_NO, T.MCDP_CD, DR_NAME, APNT_YMD FROM 
(SELECT APNT_NO, PATIENT.PT_NO, PT_NAME, MCDP_CD, APNT_YMD, MDDR_ID FROM APPOINTMENT
INNER JOIN PATIENT ON PATIENT.PT_NO = APPOINTMENT.PT_NO
WHERE APNT_CNCL_YN = "N" AND DATE_FORMAT(APNT_YMD, "%Y-%m-%d") = "2022-04-13" AND MCDP_CD = "CS") AS T
INNER JOIN DOCTOR ON DOCTOR.DR_ID = T.MDDR_ID
ORDER BY APNT_YMD