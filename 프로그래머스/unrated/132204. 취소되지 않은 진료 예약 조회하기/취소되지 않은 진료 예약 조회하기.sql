-- PATIENT 환자 정보
-- DOCTOR 의사 정보
-- APPOINTMENT 예약 정보
-- 1:1:1
SELECT APNT_NO, PT_NAME, T.PT_NO, MCDP_CD, DR_NAME, APNT_YMD 
FROM (SELECT APNT_NO, PT_NO, DOCTOR.MCDP_CD, DR_NAME,APNT_YMD FROM APPOINTMENT
INNER JOIN DOCTOR ON APPOINTMENT.MDDR_ID = DOCTOR.DR_ID
WHERE DOCTOR.MCDP_CD = "CS" 
AND DATE_FORMAT(APNT_YMD, "%Y-%m-%d") = "2022-04-13"
AND APNT_CNCL_YN = "N") AS T
INNER JOIN PATIENT ON T.PT_NO = PATIENT.PT_NO
ORDER BY APNT_YMD