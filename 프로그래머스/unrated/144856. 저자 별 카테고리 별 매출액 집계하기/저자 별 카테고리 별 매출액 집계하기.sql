-- 22년 1월, 저자, 카테고리별 매출액을 구해 저자 ID, 카테고리 DESC 순으로 구하기
SELECT AUTHOR_ID, AUTHOR_NAME, CATEGORY, SUM(SALES*PRICE) AS TOTAL_SALES
FROM (SELECT BOOK_ID, CATEGORY, BOOK.AUTHOR_ID, PRICE, AUTHOR_NAME FROM BOOK
LEFT JOIN AUTHOR ON BOOK.AUTHOR_ID = AUTHOR.AUTHOR_ID) AS T
LEFT JOIN BOOK_SALES ON T.BOOK_ID = BOOK_SALES.BOOK_ID
WHERE DATE_FORMAT(SALES_DATE, "%Y-%m") = "2022-01"
GROUP BY AUTHOR_ID, CATEGORY
ORDER BY AUTHOR_ID, CATEGORY DESC