-- 보호소 당시 중성화 X지만 나갈때 중성화된 동물의 ID, 종, 이름을 아이디 순으로 조회
SELECT ANIMAL_OUTS.ANIMAL_ID, ANIMAL_INS.ANIMAL_TYPE, ANIMAL_INS.NAME FROM ANIMAL_OUTS
INNER JOIN ANIMAL_INS ON ANIMAL_OUTS.ANIMAL_ID = ANIMAL_INS.ANIMAL_ID
WHERE (SEX_UPON_INTAKE NOT LIKE "%Spayed%" AND SEX_UPON_INTAKE NOT LIKE "%Neutered%")
AND (SEX_UPON_OUTCOME LIKE "%Spayed%" OR SEX_UPON_OUTCOME LIKE "%Neutered%")
ORDER BY ANIMAL_OUTS.ANIMAL_ID