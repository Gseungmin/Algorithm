-- 제일 비싼 식품 분류, 가격, 이름 조회
-- 식품 분류별 제일 비싼 가격
-- 해당 가격을 가진 식품의 가격 이름 조회
SELECT T.CATEGORY, PRICE AS MAX_PRICE, PRODUCT_NAME FROM FOOD_PRODUCT
INNER JOIN (SELECT CATEGORY, MAX(PRICE) AS MP FROM FOOD_PRODUCT
GROUP BY CATEGORY
HAVING CATEGORY IN ('과자', '국', '김치', '식용유')) AS T ON T.CATEGORY = FOOD_PRODUCT.CATEGORY
WHERE T.MP = FOOD_PRODUCT.PRICE
ORDER BY MAX_PRICE DESC