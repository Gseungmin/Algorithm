-- 과자 국 김치 식용류의 가격이 가장 비싼 것 조회, 가격 기준 내림차순
SELECT T.CATEGORY, MAX_PRICE, PRODUCT_NAME FROM FOOD_PRODUCT
INNER JOIN
(SELECT CATEGORY, MAX(PRICE) AS MAX_PRICE FROM FOOD_PRODUCT
WHERE CATEGORY IN ('과자', '국', '김치', '식용유')
GROUP BY CATEGORY) AS T ON T.CATEGORY = FOOD_PRODUCT.CATEGORY
WHERE PRICE = MAX_PRICE
ORDER BY MAX_PRICE DESC