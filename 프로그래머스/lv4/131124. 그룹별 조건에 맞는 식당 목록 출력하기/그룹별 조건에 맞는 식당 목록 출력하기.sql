
SELECT MEMBER_PROFILE.MEMBER_NAME AS MEMBER_NAME, REST_REVIEW.REVIEW_TEXT AS REVIEW_TEXT,
DATE_FORMAT(REST_REVIEW.REVIEW_DATE,'%Y-%m-%d') AS REVIEW_DATE
FROM REST_REVIEW
INNER JOIN MEMBER_PROFILE ON REST_REVIEW.MEMBER_ID = MEMBER_PROFILE.MEMBER_ID
WHERE REST_REVIEW.MEMBER_ID IN
    (
        SELECT REST_REVIEW.MEMBER_ID AS MEMBER_ID
        FROM REST_REVIEW
        INNER JOIN MEMBER_PROFILE ON REST_REVIEW.MEMBER_ID = MEMBER_PROFILE.MEMBER_ID
        GROUP BY MEMBER_ID
        HAVING COUNT(*) = 
        (
            SELECT MAX(NUM) AS MAX
            FROM 
            (
                SELECT REST_REVIEW.MEMBER_ID AS MEMEBER_ID, COUNT(*) AS NUM FROM REST_REVIEW
                INNER JOIN MEMBER_PROFILE ON REST_REVIEW.MEMBER_ID = MEMBER_PROFILE.MEMBER_ID
                GROUP BY MEMEBER_ID
            ) AS TAB
        )
    )
ORDER BY REVIEW_DATE, REVIEW_TEXT