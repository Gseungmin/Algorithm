-- 리뷰를 가장 많이 작성한 회원의 리뷰들을 조회
-- 리뷰를 가장 많이 작성한 회원들 조회 O
-- 회원의 리뷰들 조회

SELECT MEMBER_NAME, REVIEW_TEXT, DATE_FORMAT(REVIEW_DATE, "%Y-%m-%d") as REVIEW_DATE FROM REST_REVIEW
INNER JOIN 
(SELECT REST_REVIEW.MEMBER_ID, MEMBER_NAME, COUNT(*) AS REVIEW_CNT FROM REST_REVIEW
INNER JOIN MEMBER_PROFILE ON MEMBER_PROFILE.MEMBER_ID = REST_REVIEW .MEMBER_ID
GROUP BY MEMBER_ID
HAVING REVIEW_CNT = 
(
    SELECT COUNT(*) AS CNT FROM REST_REVIEW
    GROUP BY MEMBER_ID
    ORDER BY CNT DESC
    LIMIT 1
)) AS T ON T.MEMBER_ID = REST_REVIEW.MEMBER_ID
ORDER BY REVIEW_DATE, REVIEW_TEXT