-- 년 월 설별 별로 상품을 구매한 회원수를 집계
SELECT YEAR(SALES_DATE) AS YEAR, MONTH(SALES_DATE) AS MONTH, GENDER, COUNT(DISTINCT(USER_INFO.USER_ID)) AS USERS FROM ONLINE_SALE
LEFT JOIN USER_INFO ON USER_INFO.USER_ID = ONLINE_SALE.USER_ID
GROUP BY YEAR, MONTH, GENDER
HAVING GENDER IN (0,1)
ORDER BY YEAR, MONTH, GENDER
