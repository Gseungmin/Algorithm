import sys
input = sys.stdin.readline
N = int(input())
dp = [[[[0]*2 for k in range(4)] for j in range(3)] for i in range(N+1)]
dp[0][0][0][0] = 1
mod = 1000000007
for i in range(1,N+1):
    if dp[i-1][0][0][0] != 0:
        dp[i][0][0][0] += dp[i-1][0][0][0]
        dp[i][0][0][0] %= mod
        dp[i][1][1][0] += dp[i-1][0][0][0]
        dp[i][1][1][0] %= mod
        dp[i][1][2][1] += dp[i-1][0][0][0]
        dp[i][1][2][1] %= mod
    if dp[i-1][0][0][1] != 0:
        dp[i][0][0][1] += dp[i-1][0][0][1]
        dp[i][0][0][1] %= mod
        dp[i][1][1][1] += dp[i-1][0][0][1]
        dp[i][1][1][1] %= mod
        dp[i][1][2][1] += dp[i-1][0][0][1]
        dp[i][1][2][1] %= mod
    if dp[i-1][1][1][0] != 0:
        dp[i][0][0][0] += dp[i-1][1][1][0]
        dp[i][0][0][0] %= mod
        dp[i][2][2][0] += dp[i-1][1][1][0]
        dp[i][2][2][0] %= mod
        dp[i][2][3][1] += dp[i-1][1][1][0]
        dp[i][2][3][1] %= mod
    if dp[i-1][1][1][1] != 0:
        dp[i][0][0][1] += dp[i-1][1][1][1]
        dp[i][0][0][1] %= mod
        dp[i][2][2][1] += dp[i-1][1][1][1]
        dp[i][2][2][1] %= mod
        dp[i][2][3][1] += dp[i-1][1][1][1]
        dp[i][2][3][1] %= mod
    if dp[i-1][1][2][1] != 0:
        dp[i][0][0][1] += dp[i-1][1][2][1]
        dp[i][0][0][1] %= mod
        dp[i][2][3][1] += dp[i-1][1][2][1]
        dp[i][2][3][1] %= mod
    if dp[i-1][2][2][0] != 0:
        dp[i][0][0][0] += dp[i-1][2][2][0]
        dp[i][0][0][0] %= mod
    if dp[i-1][2][2][1] != 0:
        dp[i][0][0][1] += dp[i-1][2][2][1]
        dp[i][0][0][1] %= mod
    if dp[i-1][2][3][1] != 0:
        dp[i][0][0][1] += dp[i-1][2][3][1]
        dp[i][0][0][1] %= mod

print(dp[N][0][0][1]%mod)